function readInitialLanguage() {
  if (typeof window === 'undefined') return DEFAULT_LANGUAGE;
  const saved = window.localStorage.getItem(STORAGE_KEY);
  return SUPPORTED_LANGUAGES.includes(saved) ? saved : DEFAULT_LANGUAGE;
}

const language = ref(readInitialLanguage());

export function useI18n() {
  const locale = readonly(language);
  const isChinese = computed(() => language.value === 'zh');

  function setLanguage(nextLanguage) {
    if (!SUPPORTED_LANGUAGES.includes(nextLanguage)) return;
    language.value = nextLanguage;
    if (typeof window !== 'undefined') {
      window.localStorage.setItem(STORAGE_KEY, nextLanguage);
      document.documentElement.lang = nextLanguage === 'zh' ? 'zh-CN' : 'en';
    }
  }

  function toggleLanguage() {
    setLanguage(language.value === 'en' ? 'zh' : 'en');
  }

  function t(key, params = {}) {
    const dictionary = messages[language.value] || messages.en;
    let text = dictionary[key] ?? messages.en[key] ?? key;
    for (const [name, value] of Object.entries(params)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
    return text;
  }

  if (typeof document !== 'undefined') {
    document.documentElement.lang = language.value === 'zh' ? 'zh-CN' : 'en';
  }

  return {
    isChinese,
    locale,
    setLanguage,
    t,
    toggleLanguage,
  };
}
